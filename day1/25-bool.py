# Two Boolean values: True and False
print(True)
print(False)

# Converting other types to Boolean:
# A non-zero number is True
# A non-empty string is True

if 3:
    print('OK 3')

if 0:
    print('OK 0')

if -1:
    print('OK -1')

if '':
    print('OK ""')

if 'a':
    print('OK "a"')

if 'False':
    print('OK "False"')

if '0':
    print('OK "0"')
