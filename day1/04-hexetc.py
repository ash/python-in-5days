# Non-10-based numbers
# A decimal number
i = 1234

# Hexadecimal digits
# 0 1... 9 A B C D E F

# Hexadecimal value
i = 0xAB
print(i)

# Octal digits and octal value (in Python 2, you can use 0 as a prefix: 065 is octal)
# 0...7
i = 0o65
print(i)

# Binary digits and a binary value
# 0 1
i = 0b10111
print(i)
