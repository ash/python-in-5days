# Data types: numbers

# Integers

i = 1234
i = -24
i = 0
print(i)

print(33)

# Floating-point numbers
3.14
3.14E-1
.1
1.

# Integer or fractional parts are optional.
print(.3)
print(3.)

i = 0.3

# This is how you can see the type of a variable
print(type(i))


# Complex numbers (use "j" instead of "i")
cmplx = 3 + 2j
print(cmplx)
